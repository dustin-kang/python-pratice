def dfs(x, graph):
    stack = []
    stack.append(x)
    # ëª¨ë“  ë„ì‹œì˜ ìµœë‹¨ ê±°ë¦¬ ì´ˆê¸°í™”
    distance = [-1] * (n+1)
    distance[x] = 0
    
    while stack:
        node = stack.pop()

        for next_node in graph[node]:
            if distance[next_node] == -1: # ë°©ë¬¸í•˜ëŠ” ë…¸ë“œê°€ ì²˜ìŒ ë°©ë¬¸ í•˜ëŠ” ê²½ìš°
                distance[next_node] = distance[node] + 1
                stack.append(next_node)

    return distance

check = False
