n, m, k, x = map(int, input().split()) #ë„ì‹œ, ë„ë¡œ, íŠ¹ì • ê±°ë¦¬, ì‹œì‘ì 

# ğŸ’¡ 2ì°¨ì› ë°°ì—´ ë§Œë“¤ê¸°
graph = [[] for _ in range(n + 1)]

